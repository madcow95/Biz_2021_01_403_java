package com.callor.book;

public class Page_117 {
	
	public static void main(String args[]) {
		
		for(int dan = 3 ; dan <= 7 ; dan++) {
			
			for(int i = 1 ; i <= 9 ; i++) {
				
				System.out.println(dan + " x " + i + " = " + (dan * i));
				
			}
			System.out.println("=====================");
		}
		
	}

}
